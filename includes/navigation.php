        	<h2>Movie</h2>
        </header>
        <content>
        	<p>A movie, also called a film or motion picture, is a series of still images which, when shown on a screen creates the illusion of moving images due to the phi phenomenon. This optical illusion causes the audience to perceive continuous motion between separate objects viewed rapidly in succession. A film is created by photographing actual scenes with a motion picture camera; by photographing drawings or miniature models using traditional animation techniques; by means of CGI and computer animation; or by a combination of some or all of these techniques and other visual effects.</p>
